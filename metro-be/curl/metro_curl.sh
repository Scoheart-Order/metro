    -H "Content-Type: application/json" \
    -H "Authorization: Bearer <token>" \
    -d '{
        "lineId": 1,
        "name": "苹果园方向",
        "startStationId": 1,
        "endStationId": 10
    }'

# 删除路线
curl -X DELETE http://localhost:8123/api/metro/routes/1 \
    -H "Authorization: Bearer <token>"

# 站点停靠信息操作 - Stops

# 获取所有站点停靠信息
curl -X GET http://localhost:8123/api/metro/stops

# 根据ID获取站点停靠信息
curl -X GET http://localhost:8123/api/metro/stops/1

# 获取路线下的所有站点停靠信息
curl -X GET http://localhost:8123/api/metro/stops/route/1

# 获取站点的所有停靠信息
curl -X GET http://localhost:8123/api/metro/stops/station/1

# 获取路线和站点的停靠信息
curl -X GET "http://localhost:8123/api/metro/stops/route-station?routeId=1&stationId=2"

# 创建新站点停靠信息
curl -X POST http://localhost:8123/api/metro/stops \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer <token>" \
    -d '{
        "routeId": 1,
        "stationId": 2,
        "seq": 5,
        "isTransfer": true
    }'

# 更新站点停靠信息
curl -X PUT http://localhost:8123/api/metro/stops/1 \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer <token>" \
    -d '{
        "routeId": 1,
        "stationId": 2,
        "seq": 6,
        "isTransfer": true
    }'

# 删除站点停靠信息
curl -X DELETE http://localhost:8123/api/metro/stops/1 \
    -H "Authorization: Bearer <token>" 