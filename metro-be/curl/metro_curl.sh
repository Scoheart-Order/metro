# 线路操作 - Lines

# 获取所有线路
curl -X GET http://localhost:8123/api/metro/lines

# 根据ID获取线路
curl -X GET http://localhost:8123/api/metro/lines/1

# 根据代码获取线路
curl -X GET http://localhost:8123/api/metro/lines/code/L1

# 获取线路及其路线信息
curl -X GET http://localhost:8123/api/metro/lines/1/routes

# 创建新线路
curl -X POST http://localhost:8123/api/metro/lines \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer <token>" \
    -d '{
        "name": "1号线",
        "code": "L1",
        "color": "#FF0000",
        "operator": "北京地铁公司"
    }'

