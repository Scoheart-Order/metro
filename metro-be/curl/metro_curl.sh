curl -X GET http://localhost:8123/api/metro/routes/1

# 获取线路下的所有路线
curl -X GET http://localhost:8123/api/metro/routes/line/1

# 获取路线及其站点信息
curl -X GET http://localhost:8123/api/metro/routes/1/stations

# 查找两站点之间的路线
curl -X GET "http://localhost:8123/api/metro/routes/path?fromStationId=1&toStationId=2"

